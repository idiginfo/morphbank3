Presentation by Boyce Tankersley and Greg Riccardi at the <a href="http://www.tdwg.org/conference2008/"target="blank">2008 tdwg meeting</a>. 
<br /><br />
<b>Title:</b> Data Integration Services for Biodiversity Informatics: An Example from Plant Collections and Morphbank
<br /><br />
<b>Authors:</b> Greg Riccardi (Florida State University) and Boyce Tankersley (Chicago Botanic Garden).
<br /><br />
<b>Abstract:</b>
<br />
An interoperable information system has been created that combines the Morphbank image repository, PlantCollections botanical information portal, and several collections management systems. Putting these systems together poses many challenges that are common in biodiversity informatics. Plans are in place to make the system compatible with the TDWG Access Protocol for Information Retrieval (TAPIR). TAPIR compatibility will allow small repositories to be information providers and to allow consumers to feed back information to providers.
<br/><br/>

TAPIR is TDWG project that specifies an XML-based protocol for accessing structured data that may be stored on distributed databases of varied structure. TAPIR combines and extends features of the BioCASe and DiGIR protocols to create a new means of communication between data providers and client applications using the Internet. 
<br/><br/>

PlantCollections (PC) publishes information about public gardens on the Web using the Google Base database and XML technologies. PC relies on Morphbank for storing its images and uses Morphbank services to populate images on its pages. The PC project is an important educational and scientific activity that is integrating beautiful images and garden information with taxonomic information and other scientific content.
<br/><br/>

Botanical gardens keep extensive records of their specimens, with taxonomy and locality information related to current location and natural habitat. This information is however not readily available outside of the individual gardens. The gardens typically have little informatics infrastructure and no ability to provide 24/7 servers. The infrastructure is therefore often insufficient to allow the gardens to become direct TAPIR providers.
<br/><br/>

The major challenges for PC were to provide infrastructure to aggregate and publish the databases and the photographic collections of the gardens, and to provide for information update from garden to portal and back. The PC project began with a survey of the database fields in the garden databases, the development of a common metadata schema, the deployment of that schema using Google Base, and the transfer of many images to the Morphbank image and metadata repository. 
<br/><br/>

The typical information flow to publish garden data is: 
<br/><br/>
The garden information including image metadata is pushed to Google Base
<br/>
The image files are pushed to Morphbank via ftp
<br/>
Specimen metadata is pushed from Google Base to Morphbank via Web services
<br/>
The PC Portal provides an interface to access metadata and images with active support from Morphbank
<br/><br/>

The interaction between garden database, Google Base, the PC Portal and Morphbank is accomplished using XML documents and Application Programming Interfaces (APIs) that are consistent with TAPIR. The full implementation of updates is not present and is not fully described in TAPIR.
Two major requirements for interoperability for PC are to extend the Darwin Core, Dublin Core, and TAPIR metadata standards to include support for images and annotations, and to create a suitable update specification for biodiversity metadata.
<br/><br/>

The schema for data exchange will be created from the Dublin Core and Darwin Core metadata schemas and the TAPIR schema. Minor modifications to the existing Morphbank API and schema will be necessary for conformance. The existing Morphbank services were designed to be adaptable to new schemas. The creation of these standards will be in collaboration with TDWG, GBIF and the Atlas of Living Australia.

