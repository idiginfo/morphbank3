Presentations created by Deborah Paul for the <a href="http://www.scamit.org/index.htm"target="blank">November 2008 SCAMIT meeting</a> in San Diego, California.
<br /><br />
<b>Title:</b> SCAMIT and Morphbank
<br /><br />
<b>Authors:</b> Deborah Paul, Katja Seltmann &amp; Greg Riccardi, Florida State University 
<br /><br />
<b>Abstract:</b>
<br />
A Strategy for SCAMIT to Use Morphbank as an Image Repository  

<br /><br />
The Southern California Association of Marine Invertebrate Taxonomists (SCAMIT) group was formed in 1982 with the goal of promoting the study of marine invertebrate taxonomy in southern California and developing a regionally standardized taxonomy. Furthering this goal, they are in the process of designing and building a web site. The web site will include species pages for the public as well as a secure section for SCAMIT members complete with a blog and taxonomic keys. Morphbank was chosen by SCAMIT as their image repository. 

<br /><br />
SCAMIT sent members to Tallahassee in May 2007 to the Morphbank Usability Study and Workshop. Since learning to use Morphbank via the web SCAMIT members would like to streamline the process of data and image submission. In collaboration with SCAMIT, Morphbank designed a simpler Excel workbook for users.

<br /><br />
Four SCAMIT meetings are planned for November 2008. The first meeting on November 17th focuses on a possible design for their database, an explanation of how the images will be called from Morphbank to appear on the SCAMIT pages and an introduction to the newly-designed, easily-customized Excel workbook for data submission.
 
<br /><br />
Regarding the Excel workbook, the idea is to give any Morphbank contributor a fast way to compile data for submission (as much or as little data as the user desires). In Morphbank, it is now possible to submit an image with as little metadata as a taxonomic name (can be a temporary taxonomic identifier) and a unique external identifier for the image (a user-provided GUID, LSID, etc). These changes to the Morphbank database make it possible to use Morphbank strictly as an image repository with little or no metadata regarding the specimen, view, or locality information. After sending a completed workbook to Morphbank, the contents are put in XML format for upload to Morphbank.

<br /><br />
The next 3 SCAMIT meetings, November 18th - 20th,  focus on how to use this new Excel Workbook. Each Lab (LACSD, OCSD, etc.) will be submitting data for upload to Morphbank. The GUIDs needed for each image will be assigned by a single individual in each lab, or one central person SCAMIT chooses. The powerpoint explains how to populate the workbook and where to go for help if there is a question.
