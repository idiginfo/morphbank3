<b>Title:</b> Representing and Using Phylogenetic Characters in Morphbank 
<br /><br />
<b>Authors:</b> Greg Riccardi, David Gaitros, and Fredrik Ronquist Florida State University 
<br /><br />
<b>Abstract:</b>
<br />
Integrating & sharing biotic information, Images  

