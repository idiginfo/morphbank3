The Morphbank project is a repository of biological images and related information that includes extensive user tools in Web site, at http://morphbank.net. The value of the images in the repository is increased by maintaining detailed metadata about the image and its associated specimen. The system includes bulk upload operations that make is simple to add image collections.

The Morphbank development team deployed a new version of the Web site in summer 2006 that adds extensive annotation tools for image annotations and determination annotations. A determination annotation is an object in the system that associates a specimen with an assessment of the correctness of its taxon identification. A scientist can create an annotation in order to register agreement or disagreement with the current determination. In the case of disagreement, an alternative taxon is specified. Each annotation contains image annotations that identify areas of interest related to the determination.

The specimens of the Florida State University Herbarium are now included in the MorphBank repository as image and specimen objects. The Morphbank determination annotation tools are being used to evaluate the determinations of the specimen. The tools will allow experts to record their assessments of the quality of the herbarium metadata and to correct the determinations as necessary.

The next annotation capability that will be included in the Morphbank tools is character state annotation, the association of a phylogenetic character state with a specific area of interest of an image. Development has begun on tools to create and manipulate phylogenetic characters and their states. These tools support  importing and exporting of Nexus files.

Other systems, e.g. Mesquite and MorphoBank. support the creation of character matrices that associate species with character states. Morphobank provides tools to include and annotate  images of the species.

Character state annotations in Morphbank are focused on identifying areas within images that are show particular character states. Each image may have many annotations. Morphbank image annotations can be used to create character matrices that relate species to states, and may also be used to relate specimens to states.

The search and browse capabilities of the Morphbank tools will provide scientists with capabilities to find and compare character state annotations in ways that are not currently possible. Scientists can find all images that display particular characters or states, can use image annotations to clearly identify and differentiate the states, and can easily find appropriate characters to use for their specimens. The character definitions will be shared among all users of MorphBank.

Current development plans call for the character state annotation tools to be fully implemented and deployed in advance of the TDWG 06 meeting.

The TDWG talk for this abstract will include illustrations of the use of both determination and character state annotations. Details of the data models that support annotations and demonstrations of the tools will also be included.

