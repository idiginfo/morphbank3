<b>Title:</b> OntoMorphBankSter: Image-driven Ontology and/or Ontology-driven Image Annotation
<br /><br />
<b>Authors:</b> Greg Riccardi,  Austin Mast (Florida State University), Dan Miranker, and Ferner Cilloniz (University of Texas).
<br /><br />
<b>Abstract:</b>
<br />
We describe a method of linking images and ontologies realized as a combination of two systems: the Morphbank image and metadata repository (http://www.morphbank.net/) and the Morphster ontology illustration and management system (http://www.morphster.org/). Our formal treatment of images allowed us to create an image-driven ontology editor to enable domain experts to build ontologies as a part of the research process. 
<br/><br/>
Two concepts are linked in this system: image-driven ontology, which couples images with classes and properties to provide a rich definition of the concepts described, and ontology-based image annotation, which tags images with ontology terms to identify the areas of interest within the image that are described by the terms. The two ideas combined support the notion of standard view descriptions-- combination of ontology terms that describe what can be seen in an image. A group may predefine standard views and associate appropriate views with their images. This aggregation of terms into standard views makes it easy for users to give precise descriptions of their images using terms that are understood within the group.
The combined system relies on standard interaction methods. Morphbank has Web services that support query, insert and update. Morphster provides interoperability among a variety of ontology systems. Morphster interacts with Morphbank services to create a dynamic linking between concepts and images.This integration is embodied in OntoBrowser (http://www.morphster.org:8080/OntobrowserV3/Ontobrowser.html). 
As images supplant hands-on observation of specimens in systematic research, the use of search methods to retrieve related images is a critical function. An open research question in this environment is how can semantically rich systems (i.e. ontology) exploit those semantics when accessing and retrieving images from legacy databases. In our context, OntoBrowser is a semantically rich environment. Users may choose among ontologies of different organisms. Individual images may be found by browsing by ontology terms or through a search mechanism that leverages the ontology. 
<br/><br/>
Ontology-based image retrieval systems use a basic definition of similarity that involves a graph-based distance measure among the set of ontological terms in the query image and the set of ontological terms associated with each image in the database. This is the same problem as measuring the similarity of the function of two genes per their annotation in the Gene Ontology. A search will be BLAST-like in the sense that the ontology annotations characterize the similarity between images. The results will be ranked based on the information that scientists have associated with the images.
Our ontology activities are informed by our collaboration with the Spider Assembling the Tree of Life (SATOL) project (http://research.amnh.org/atol/files/), Phenoscape (https://www.nescent.org/phenoscape) and the Hymenoptera Ontology (http://ontology.insectmuseum.org).
