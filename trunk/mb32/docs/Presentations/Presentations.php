Presentation by Fredrik Ronquist and Greg Riccardi at the <a href="http://www.tdwg.org/conference2007"target="blank">2007 tdwg meeting</a>. 
<br /><br />
<b>Title:</b> Building Relationships between Images and other Biological Objects using the Morphbank Repository (www.morphbank.net) and Morphbank Collaboratory Tools
<br /><br />
<b>Authors:</b> Greg Riccardi, Fredrik Ronquist, Austin Mast, Neelima Jammingumpula, Katja Seltmann, Karolina Maneva-Jakimoska, Steve Winner and Deborah Paul (Florida State University), and Andrew Deans (North Carolina State University).
<br /><br />
<b>Abstract:</b>
<br />
An overview presentation outlining the Morphbank project, its long-term goals and the present software.  Includes linking and embeding images, the ITIS collaboration and incorperating ontologies into the system beginning with the Hymenoptera Ontology.
<br /><br />
Morphbank was designed to be a secure repository for images and metadata.  It includes a rich data model that conforms to open community-supported standards, such as Darwin Core for specimen metadata.  The metadata is available in Web pages and in XML and RDF.  Access to content is managed by content owners and can be restricted to a specific group of users or made publicly available.  The security model ensures that researchers can take advantage of the Morphbank system during the entire research lifecycle: from initial data collection and imaging through publication. <br>
<br>
The Morphbank team has built enabling technology for biodiversity researchers, particularly users of biodiversity research collections and morphological phylogeneticists.  In the process, we have built connections to other providers of functionality (e.g., HERBIS, Specify) and biodiversity content (e.g., ITIS, the Integrated Taxonomic Information System).<br>
<br>
A major advance in the usability of Morphbank comes from collections management, which allows a user to accumulate digital objects (private and public) in collections.  Users can accumulate all of the objects used in making scientific interpretations, including images, image annotations, publications, operational taxonomic units (OTU), character matrices, and even other digital object collections.  The digital objects that can be included in a collection are not restricted to objects within the Morphbank system, but can be any digital object with a globally unique identifier (GUID).  Collections can be published, and can form the kernel around which new collections are built.  Collections provide a powerful way of documenting and publishing scientific observations and opinions. <br>
<br>
The Morphbank Collections Interface shows a collection that has been created to code a character in a matrix for phylogenetic study or the production of a multi-entry identification key.  The user has organized the image tiles in order to define a particular feature.  In this case, the character represents the occurrence of spots on butterfly wings.  The states are many spots, few spots and no spots.  Each blue tile and the thumbnails that follow it represent one of the states and some images that are coded with that state.  The character can be used in one or more matrices and additional images can be coded and added to the state.<br>
<br>
This strategy for representing character matrices is designed to enhance the interaction with systems that focus on matrices, like Morphobank and Mesquite.  By using the images as the primary source of character information, we provide a different look and feel to the character coding process.  Morphbank is a rich resource for other systems to use in the discovery and illustration of characters and states.<br>
<br>
The presentation will emphasize the connections that have been defined by researchers among objects in the Morphbank system and external objects, and the ways that these connections illustrate important research concepts.</</td>
