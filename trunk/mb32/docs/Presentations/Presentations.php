Presentation created by Andrew R. Deans et al. for the <a href="http://tdwg2006.tdwg.org/" target="blank">2006 tdwg meeting</a> 
<br /><br />
<b>Title:</b> 4.5. Ontologizing Morphological Terms for Hymenoptera (Insecta) - implementing and Benefiting from a Controlled Vocabulary
<br /><br />
<b>Authors:</b> Andrew R Deans, Gregory A Riccardi, Fredrik Ronquist, Florida State University
<br /><br />
<b>Abstract:</b>
	Hymenoptera encompasses a large group of organisms commonly referred to as sawflies, wasps, ants and bees. This group has historically had numerous communities of researchers untangling its mysteries. Sawfly specialists, parasitoid people, ant taxonomists, bee biologists, and aculeate workers have converged on a common language that describes the morphological characteristics of these insects. Each research group has however also cultivated its own specialized terminology that may not be applicable to other hymenopterans. Complications include: (1) words that apply to structures only found in certain taxa (e.g., "cenchrus" in sawflies); (2) words that are different from terms other workers use ("parapsidal furrow" in some taxa is homologous to the "notaulus" in other taxa); (3) words that are obscure and go unused ("lunule" in microgastrine Braconidae) and (4) words that have the same spelling but are defined differently depending on the taxon (e.g., "face" in Symphyta and Aculeata is not the same as "face" in Parasitica). 
	Ontologies serve multiple functions in a vast array of contexts - from facilitating communication between databases to standardizing the terminology used by a particular field. The hymenopterist community seeks to ontologize the vast array of morphological terminology by: (1) carefully reviewing the literature pertaining Hymenoptera morphology; (2) systematically designating synonyms that are either valid or obsolete; (3) defining relationships between terms and (4) illustrating and verbally defining valid terms. 
<br /><br />
Ontology development and editing: 
<br />
Several software packages are available for developing and editing ontologies, e.g. OBO-edit (Gene Ontology Consortium) and Protege-OWL (Stanford), which can export the ontology in several formats. The Hymenoptera community will likely employ a user-friendly, customizable database built using Ruby on Rails/MySQL (mx; http://hymenoptera.tamu.edu/mx/). This strategy allows more Hymenoptera morphologists to participate since there is no new software to learn, and it allows the ontology to be built remotely, from anywhere with internet access. After the ontology matures, the database will be assembled by our bioinformatics colleagues into a usable ontology. 

The standards and hierarchical nature of an ontology will prove invaluable when applied to resources such as MorphBank (http://www.morphbank.net/):
<br />
Situation 1 - When searching for "mesosoma" images, one could potentially miss images that were tagged as "scutellum". By incorporating the ontology into the search algorithm one could return images of "mesosoma" and all related terms (e.g., terms that are "part of" the mesosoma, such as the scutellum). 
<br />
Situation 2 - Annotating uploaded images. Standard terminology is highlighted and linked and inappropriate synonyms are identified, while non-standard or misspelled terms remain unrecognized. This provides feedback about the quality of the annotation. 
<br />
Situation 3 - When scoring phylogenetic characters, users might define characters and states differently. An ontology will assist in homology assignments critical to phylogenetic analyses. 
